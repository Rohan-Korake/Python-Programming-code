# Addition
def add(num1,num2):
    return num1+num2

# Subtraction
def sub(num1,num2):
    return num1-num2

# Multiplication
def multiply(num1,num2):
    return num1*num2

# Division
def div(num1,num2):
    return num1/num2

# Modulus
def mod(num1,num2):
    return num1%num2

# Square
def sqr(num1):
    return num1*num1