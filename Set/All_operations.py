# Create two sets
set1 = {1, 2, 3, 4}
set2 = {3, 4, 5, 6}

print("Set 1:", set1)
print("Set 2:", set2)

# Union
print("Union:", set1 | set2)

# Intersection
print("Intersection:", set1 & set2)

# Difference (set1 - set2)
print("Difference (Set1 - Set2):", set1 - set2)

# Symmetric Difference
print("Symmetric Difference:", set1 ^ set2)
